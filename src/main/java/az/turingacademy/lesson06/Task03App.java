package az.turingacademy.lesson06;

public class Task03App {

    public static void main(String[] args) {
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");
        }

        System.out.println();

        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");
        }

    }

}
